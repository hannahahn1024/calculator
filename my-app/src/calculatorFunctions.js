// Addition function
export function add(a, b) {
    return a + b;
  }
  
  // Subtraction function
  export function subtract(a, b) {
    return a - b;
  }
  
  // Multiplication function
  export function multiply(a, b) {
    return a * b;
  }
  
  // Division function
  export function divide(a, b) {
    return a / b;
  }
  